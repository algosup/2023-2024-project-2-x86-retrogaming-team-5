section .bss
keyPressed resb 1  ; Reserve a byte for the state of the key
section .text
start:
; Initializing video mode
    mov ah, 00h
    mov al, 13h
    int 10h
    
; Main game loop

       
    
    gameloop:
        call clear_old_sprite
        call draw_sprit
        call read_character_key_was_pressed
        
        ; Delay to slow down animation
        mov cx, 64000
       waitloop:
       loop waitloop
        jmp gameloop
 ; End of game, return to text mode
    mov ax, 03h
    int 10h
    mov ax, 4C00h
    int 21h
; Function to read key pressed
read_character_key_was_pressed:
    mov ah, 00h
    int 16h
    cmp ah, 0E0h
    jne handle_input
    ret

handle_input:
    cmp ah, 4Dh   ; Right Key ;4DH                          ; Down key ;50h
    je move_right
    cmp ah, 4Bh    ; Left Key ;4BH   
    je move_left
    cmp ah, 48h   ; Up key ;48H        
    je move_up
    cmp ah, 50h  ; Down key ;50h
    je move_down
    ret

    ret

clear_old_sprite:
   
; Clear sprite at old position
    mov ax, 0A000h
    mov es, ax
    neg word [xVelocity]  ; Y * screen width
    add bx, [old_XPOS]  ; + X
    mov di, bx         ; DI = starting address for erasure
    mov cx, SPRITEH    ; Sprite height
.clear_line:
    push cx
    mov cx, SPRITEW    ; Sprite width
    mov al, 0          ; Background color for erasing
    rep stosb          ; Clear pixels with background color
    pop cx
    add di, 320 - SPRITEW  ; Move to next line for deletion
    loop .clear_line
    ret

move_right:
    call boucle_changeright
    mov bx, [xPos]
    add bx, 1 
    mov [xPos], bx
    ret
move_left:
    call boucle_changeleft
    mov bx, [xPos]
    sub bx, 1
    mov [xPos], bx
    ret

move_up:
    call boucle_changeUp
    mov bx, [xPos]
    sub bx, S_WIDTH
    mov [xPos], bx
    ret

move_down:
    mov bx, [xPos]
    add bx,  S_WIDTH
    mov [xPos], bx
    ret


   boucle_changeright:
        cmp si, pacmanClose
        jne .pacmanopnd
        jmp .pacmanclosd

    .pacmanopnd:
       mov si, pacmanOpen
       mov di,100
       ret
     .pacmanclosd:
        mov si, pacmanClose
        mov di, 100
        ret

    boucle_changeleft:
        cmp si, pLeftClose
        jne .pacmanopend
        jmp .pacmanclosed

    .pacmanopend:
       mov si, pLeftOpen
       mov di, 100
       ret
     .pacmanclosed:
        mov si, pLeftClose
        mov di, 100
        ret
 
     boucle_changeUp:
        cmp si, pUpClose
        jne .pacmanopend
        jmp .pacmanclosed

    .pacmanopend:
       mov si, pUpOpen
       mov di, 100
       ret
     .pacmanclosed:
        mov si, pUpClose
        mov di, 100
        ret

; Function to clear the screen
draw_sprit:
    mov ax, 0xA000
    mov es, ax
    mov dx, 16
            .eachLine:
            mov cx, 16
            rep movsb
        add di, S_WIDTH-16
        dec dx
        jnz .eachLine
    ret

